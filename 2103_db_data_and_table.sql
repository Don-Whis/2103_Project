CREATE SCHEMA IF NOT EXISTS 2103_tabledata;
use 2103_tabledata;
/****************** ALL ENTIY CREATION ******************/
CREATE TABLE 2103_tabledata.HealthStatus(
    HealthStatus_ID INT,
    Vaccinated BIT,  /*Boolean value*/
    CovidPositive BIT,
    PRIMARY KEY (HealthStatus_ID)
);
CREATE TABLE 2103_tabledata.DailyHealthDeclaration(
    HealthDeclaration_ID INT,
    Temperature FLOAT,
    DateTaken DATE,
    PRIMARY KEY (HealthDeclaration_ID)
);
CREATE TABLE 2103_tabledata.Student(
    Student_ID INT,
    Student_Name VARCHAR(128) NOT NULL,
    Student_Email VARCHAR(128),
    Gender CHAR(1) CHECK (Gender = 'M' OR Gender = 'F'),
    Age INT,
    PRIMARY KEY (Student_ID)
);
CREATE TABLE 2103_tabledata.Lesson(
    Lesson_ID VARCHAR(128),
    LessonDate DATE,
    Start_Time TIME,
    End_Time TIME,
    PRIMARY KEY (Lesson_ID)
);
CREATE TABLE 2103_tabledata.Professor(
    Professor_ID INT,
    Professor_Name VARCHAR(128) NOT NULL,
    Professor_Email VARCHAR(128) NOT NULL,
    Contact_Number INT,
    Department VARCHAR(128),            /*Might want to add it as another entity*/
    Gender CHAR(1) CHECK (Gender = 'M' OR Gender = 'F'),
    PRIMARY KEY (Professor_ID)
);
CREATE TABLE 2103_tabledata.Module(
    Module_ID VARCHAR(128),
    Module_Name VARCHAR(128) NOT NULL,
    Inclass BIT,
    PRIMARY KEY (Module_ID)
);
CREATE TABLE 2103_tabledata.Classroom(
    Classroom_ID VARCHAR(128),
    Classroom_Name VARCHAR(128) NOT NULL,
    Classroom_Floor VARCHAR(128) NOT NULL,
    PRIMARY KEY (Classroom_ID)
);
CREATE TABLE 2103_tabledata.Building(
    Building_ID INT,
    Building_Name VARCHAR(128),
    PRIMARY KEY (Building_ID)
);

/****************** ALL RELATIONSHIP CREATION ******************/
CREATE TABLE 2103_tabledata.STUDENT_HAS(
    HealthStatus_ID INT,
    Student_ID INT,
    PRIMARY KEY (HealthStatus_ID, Student_ID),
    FOREIGN KEY (HealthStatus_ID) REFERENCES HealthStatus(HealthStatus_ID),
    FOREIGN KEY (Student_ID) REFERENCES Student(Student_ID)
);
CREATE TABLE 2103_tabledata.STUDENT_DHD(
    HealthDeclaration_ID INT,
    Student_ID INT,
    PRIMARY KEY (HealthDeclaration_ID),
    FOREIGN KEY (Student_ID) REFERENCES Student(Student_ID)
);
CREATE TABLE 2103_tabledata.PROFESSOR_HAS(
    HealthStatus_ID INT,
    Professor_ID INT,
    PRIMARY KEY (HealthStatus_ID, Professor_ID),
    FOREIGN KEY (HealthStatus_ID) REFERENCES HealthStatus(HealthStatus_ID),
    FOREIGN KEY (Professor_ID) REFERENCES Professor(Professor_ID)
);
CREATE TABLE 2103_tabledata.PROFESSOR_DHD(
    HealthDeclaration_ID INT,
    Professor_ID INT,
    PRIMARY KEY (HealthDeclaration_ID),
    FOREIGN KEY (Professor_ID) REFERENCES Professor(Professor_ID)
);
CREATE TABLE 2103_tabledata.ATTEND(
    Lesson_ID VARCHAR(128),
    Student_ID INT,
    FOREIGN KEY (Lesson_ID) REFERENCES Lesson(Lesson_ID),
    FOREIGN KEY (Student_ID) REFERENCES Student(Student_ID)
);
CREATE TABLE 2103_tabledata.TEACH_BY(
    Lesson_ID VARCHAR(128),
    Professor_ID INT,
    FOREIGN KEY (Lesson_ID) REFERENCES Lesson(Lesson_ID),
    FOREIGN KEY (Professor_ID) REFERENCES Professor(Professor_ID)
);
CREATE TABLE 2103_tabledata.TAKE_PLACE(
    Lesson_ID VARCHAR(128),
    Classroom_ID VARCHAR (128),
    FOREIGN KEY (Lesson_ID) REFERENCES Lesson(Lesson_ID),
    FOREIGN KEY (Classroom_ID) REFERENCES Classroom(Classroom_ID)
);
CREATE TABLE 2103_tabledata.CONTAIN(
    Module_ID VARCHAR(128),
    Lesson_ID VARCHAR(128),
    FOREIGN KEY (Module_ID) REFERENCES Module(Module_ID),
    FOREIGN KEY (Lesson_ID) REFERENCES Lesson(Lesson_ID)
);
CREATE TABLE 2103_tabledata.BELONGS_TO(
    Classroom_ID VARCHAR(128),
    Building_ID INT,
    PRIMARY KEY (Classroom_ID, Building_ID),
    FOREIGN KEY (Classroom_ID) REFERENCES Classroom(Classroom_ID),
    FOREIGN KEY (Building_ID) REFERENCES Building(Building_ID)
);
